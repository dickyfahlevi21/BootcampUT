// Grade
let grade = 90; //Input your number!
let result = "";

if (grade >= 90) {
    console.log(result = "A")
} else if (grade >= 80) {
    console.log(result = "B")
} else if (grade >= 70) {
    console.log(result = "C")
} else if (grade >= 60) {
    console.log(result = "D")
} else {
    console.log(result = "E")
}